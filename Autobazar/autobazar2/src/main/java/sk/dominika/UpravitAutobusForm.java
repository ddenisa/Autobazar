    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        autobus.setDruh(cmbDruh.getSelectedItem().toString());
        autobus.setZnacka(cmbZnacka.getSelectedItem().toString());
        autobus.setModel(txtModel.getText());
        int rok = Integer.valueOf((String) cmbRokVyroby.getSelectedItem());
        autobus.setRokVyroby(rok);
        autobus.setNajazdeneKm(Integer.parseInt(txtNajazdeneKm.getText()));
        autobus.setCena(new BigDecimal(txtCena.getText()));
        
        autobusDao.save(autobus);
        
        dispose();
    }//GEN-LAST:event_btnOKActionPerformed

