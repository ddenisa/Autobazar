    public UpravitAutobusForm(Autobus autobus, Frame parent) {
        this(parent, true);
        this.autobus = autobus;

        if (autobus.getID() != -1) {
            cmbDruh.setSelectedItem(autobus.getDruh());
            cmbZnacka.setSelectedItem(autobus.getZnacka());
            txtModel.setText(autobus.getModel());
            Integer rok = autobus.getRokVyroby();
            cmbRokVyroby.setSelectedItem(rok.toString());
            Integer najazdene = autobus.getNajazdeneKm();
            txtNajazdeneKm.setText(najazdene.toString());
            txtCena.setText(autobus.getCena().toString());
        }

    }

